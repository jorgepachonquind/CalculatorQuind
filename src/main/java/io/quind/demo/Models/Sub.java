package io.quind.demo.Models;

public class Sub {
    int num1 = 0;
    int num2 = 0;
    int result = 0;

    public Sub(int num1, int num2, int result) {
        this.num1 = num1;
        this.num2 = num2;
        this.result = result;
    }

    public Sub() { }

    public int sub ( int num1, int num2 ){ return result = num1 - num2; }
}
